AND R2, R2, #0
AND R3, R3, #0
AND R4, R4, #0
AND R5, R5, #0
LDI R1, ORI
AND R0, R0, #1
BRNP #2
NOT R1, R1
ADD R1, R1, #1
ADD R5, R5, #1
ADD R2, R2, #15
ADD R2, R2, #1
LOOP AND R4, R1, R5
BRNP #1
ADD R3, R3, #1
ADD R5, R5, R5
ADD R2, R2, #-1
BRP LOOP
LDI R2, STU_NUM
ADD R3, R3, R2
STI R3, RES
HALT
